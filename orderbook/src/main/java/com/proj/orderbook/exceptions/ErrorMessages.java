package com.proj.orderbook.exceptions;

public class ErrorMessages {

	private String errorMessage;
	private String errorOccuredTime;

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}

	public String getErrorOccuredTime() {
		return errorOccuredTime;
	}

	public void setErrorOccuredTime(String errorOccuredTime) {
		this.errorOccuredTime = errorOccuredTime;
	}
}
